bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  switch (keycode) {
    case WOKMOK:
      if (record->event.pressed) {
        set_single_persistent_default_layer(_WOKMOK);
      }
      return false;
      break;
    case LOWER:
      if (record->event.pressed) {
        layer_on(_LOWER);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      } else {
        layer_off(_LOWER);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      }
      return false;
      break;
    case RAISE:
      if (record->event.pressed) {
        layer_on(_RAISE);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      } else {
        layer_off(_RAISE);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      }
      return false;
      break;
    case ADJUST:
      if (record->event.pressed) {
        layer_on(_ADJUST);
      } else {
        layer_off(_ADJUST);
      }
      return false;
      break;
  }
  return true;
}

bool encoder_update_user(uint8_t index, bool clockwise) {
    if (index == 0) {               // left encoder (not installed on keeb)
        if (clockwise) {
            tap_code(KC_VOLU);
        } else {
            tap_code(KC_VOLD);
        }
    }
    else if (index == 1) {  // right encoder
      if (layer_state_is(_WOKMOK)) {
         if (clockwise) {
            tap_code(KC_PGDN);
         } else {
            tap_code(KC_PGUP);
         }
      } else if (layer_state_is(_LOWER)) {
         if (clockwise) {
            tap_code16(C(KC_TAB));
         } else {
            tap_code16(S(C(KC_TAB)));
         }
      } else if (layer_state_is(_RAISE)) {
         if (clockwise) {
            tap_code16(RGB_MOD);
         } else {
            tap_code16(RGB_RMOD);
         } 
      }
   }
   return false;
}
